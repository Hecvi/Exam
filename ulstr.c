#include <unistd.h>

int		main(int ac, char **av)
{
	if (2 == ac)
	{
		while (*av[1])
		{
			if ('a' <= *av[1] && *av[1] <= 'z')
				*av[1] -= 32;
			else if ('A' <= *av[1] && *av[1] <= 'Z')
				*av[1] += 32;
			write(1, av[1], 1);
			av[1]++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
