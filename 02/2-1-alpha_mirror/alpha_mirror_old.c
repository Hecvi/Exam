#include <unistd.h>

int		main(int ac, char **a)
{
	int i;

	if (ac == 2)
	{
		i = 0;
		while (a[1][i])
		{
			if (a[1][i] >= 'a' && a[1][i] <= 'z')
				a[1][i] = 'z' - (a[1][i] - 'a');
			else if (a[1][i] >= 'A' && a[1][i] <= 'Z')
				a[1][i] = 'Z' - (a[1][i] - 'A');
			write(1, &a[1][i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
