#include <unistd.h>

int		main(int ac, char **av)
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (3 == ac)
	{
		while (av[1][i])
			i++;
		while (av[1][j])
		{
			while (*av[2])
			{
				if (*av[2] == av[1][j])
				{
					i--;
					av[2]++;
					break ;
				}
				av[2]++;
			}
			j++;
		}
		if (0 == i)
			while (av[1][i])
			{
				write(1, &av[1][i], 1);
				i++;
			}
	}
	write(1, "\n", 1);
	return (0);
}
