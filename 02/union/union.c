#include <unistd.h>

int		main(int ac, char **av)
{
	int check[256] = {0};

	if (3 == ac)
	{
		while (*av[1])
		{
			if (!check[*av[1]])
			{
				check[*av[1]] = 1;
				write(1, av[1], 1);
			}
			av[1]++;
		}
		while (*av[2])
		{
			if (!check[*av[2]])
			{
				check[*av[2]] = 1;
				write(1, av[2], 1);
			}
			av[2]++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
