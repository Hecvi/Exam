#include <unistd.h>

int		main(int argc, char **argv)
{
	int i;

	i = 0;
	if (argc != 2)
	write(1, "a\n", 2);
	else
	{
		while (argv[1][i] != '\0' && argv[1][i] != 'a')
			i++;
		if (argv[1][i] == 'a')
		{
			write(1, &argv[1][i], 1);
			write(1, "\n", 1);
		}
		else
			write(1, "\n", 1);
	}
	return (0);
}
