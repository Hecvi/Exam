#include <unistd.h>

int		main(void)
{
	char c;
	char k;

	c = 'a';
	while (c <= 'z')
	{
		write(1, &c, 1);
		c++;
		k = c - 32;
		write(1, &k, 1);
		c++;
	}
	write(1, "\n", 1);
	return (0);
}
