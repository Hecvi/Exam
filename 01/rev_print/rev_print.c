#include <unistd.h>

int		main(int ac, char **av)
{
	int i;

	if (2 == ac && av[1][i] != '\0')
	{
		i = 0;
		while (av[1][i + 1])
			i++;
		while (i >= 0)
			write(1, &av[1][i--], 1);
	}
	write(1, "\n", 1);
	return (0);
}
