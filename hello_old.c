#include <unistd.h>

int		main(void)
{
	int i;
	char *str;

	i = 0;
	str = "Hell";
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
